            <p align="center">La Ford Motor Company, más conocida como Ford, es una empresa multinacional de origen estadounidense, especializada en la industria automotriz.
 <p>
Con su sede central ubicada en Dearborn, Estado de Michigan, la empresa se expandió a nivel mundial destacándose principalmente en el rubro de producción de automóviles. La compañía tiene presencia a nivel mundial, gracias a sus filiales ubicadas en Alemania, Argentina, Brasil, Canadá, Chile, España, India, México, Tailandia y Venezuela, entre otros. Fue fundada por Henry Ford y se incorporó el 16 de junio de 1903. La compañía vende automóviles y vehículos comerciales bajo la marca Ford y la mayoría de los coches de lujo bajo la marca Lincoln. Ford también posee el fabricante brasileño de SUV's Troller, y el australiano FPV. En el pasado, también se han producido tractores y componentes de automoción. Ford posee una participación del 8% en Aston Martin del Reino Unido, y una participación del 49% en Jiangling de China. También tiene una serie de empresas conjuntas, una en China (Changan Ford), una en Taiwán (Ford Lio Ho), una en Tailandia (AutoAlliance Thailand), una en Turquía (Ford Otosan), y una en Rusia (Ford Sollers). Sus operaciones en Europa si bien estuvieron representadas por las distintas filiales de Ford en países como Alemania, Bélgica, Irlanda o el Reino Unido, actualmente están todas reunidas bajo el paraguas de una sociedad denominada Blue Oval Holdings que actualmente lleva adelante las acciones de Ford Europa.</p>
<p>
Está inscrita en la Bolsa de Nueva York y es controlada por la familia Ford, aunque tienen la propiedad minoritaria (pero la mayoría de los derechos de voto).1​2​


Sede central de Ford en Dearborn. </p>
<p>
Ford Fusion 2006
Ford introdujo métodos para la fabricación a gran escala de los coches y la gestión a gran escala de una fuerza de trabajo industrial utilizando secuencias de fabricación elaborada por ingeniería tipificados por movimiento de líneas de ensamble. En 1914, estos métodos fueron conocidos en todo el mundo como el fordismo. Ex filiales británicas de Ford: Jaguar y Land Rover, adquiridas en 1989 y 2000, respectivamente, fueron vendidas a Tata Motors en marzo de 2008. Ford era el dueño del fabricante de automóviles sueco Volvo desde 1999 hasta 2010.3​ En 2011, Ford suspendió la marca Mercury, en virtud del cual se había comercializado coches de lujo de nivel de entrada en los Estados Unidos, Canadá, México, y el Medio Oriente desde 1938. Durante la crisis financiera a principios del siglo 21, estuvo cerca de la quiebra, pero desde entonces ha vuelto a ser rentable.</p>


Ford Explorer de quinta generación.
Ford es el segundo mayor fabricante de automóviles con sede en E.E.U.U (precedido por General Motors) y el quinto más grande en el mundo (por detrás de Toyota, Volkswagen, Hyundai Motor Group y General Motors) basado en la producción de vehículos de 2015. A finales de 2010, Ford fue el quinto mayor fabricante de automóviles en Europa.4​ Ford es la empresa con sede en América en general clasificado en octavo lugar en 2010 de Fortune 500 lista, sobre la base de los ingresos globales en 2009 de $ 118,3 mil millones.5​ En 2008, Ford produjo 5,532 millones de automóviles y empleó a cerca de 213.000 empleados en alrededor de 90 plantas e instalaciones en todo el mundo.6​ La compañía salió a bolsa en 1956, pero la familia Ford, a través de acciones especiales de Clase B, todavía conservan 40 por ciento de los derechos de voto.2​7​En el año 2015 Ford Motor Company cierra el ejercicio del año 2014 con un total de 6,32 millones de unidades vendidas y 144.100 millones de dólares de ingresos. </p> 

    
